@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun HistoryScreen(
    onNavigateBack: (() -> Unit)? = null,
    showTopBar: Boolean = false
) {
    var selectedTab by remember { mutableStateOf(0) }
    var selectedDate by remember { mutableStateOf(LocalDate.now()) }
    var currentMonth by remember { mutableStateOf(YearMonth.now()) }

    val sampleData = remember { getSampleData() }

    Column(
        modifier = Modifier
            .fillMaxSize()
            .background(MaterialTheme.colorScheme.background)
    ) {
        // ÏÉÅÎã® Ï†úÎ™© (showTopBarÍ∞Ä falseÏùº ÎïåÎßå ÌëúÏãú)
        if (!showTopBar) {
            Text(
                text = "Í≥ºÍ±∞ Í∏∞Î°ù",
                fontSize = 24.sp,
                fontWeight = FontWeight.Bold,
                modifier = Modifier.padding(16.dp)
            )
        }

        // ÌÉ≠ ÏÑπÏÖò
        TabRow(selectedTabIndex = selectedTab) {
            Tab(
                selected = selectedTab == 0,
                onClick = { selectedTab = 0 }
            ) {
                Text("ÏùºÎ≥Ñ", modifier = Modifier.padding(vertical = 12.dp))
            }
            Tab(
                selected = selectedTab == 1,
                onClick = { selectedTab = 1 }
            ) {
                Text("ÌÜµÍ≥Ñ", modifier = Modifier.padding(vertical = 12.dp))
            }
        }

        // ÌÉ≠ ÎÇ¥Ïö©
        when (selectedTab) {
            0 -> DailyTab(
                selectedDate = selectedDate,
                onDateSelected = { selectedDate = it },
                currentMonth = currentMonth,
                onMonthChanged = { currentMonth = it },
                usageData = sampleData
            )
            1 -> StatisticsTab()
        }
    }
}

@Composable
fun DailyTab(
    selectedDate: LocalDate,
    onDateSelected: (LocalDate) -> Unit,
    currentMonth: YearMonth,
    onMonthChanged: (YearMonth) -> Unit,
    usageData: Map<LocalDate, DayUsageData>
) {
    LazyColumn(
        modifier = Modifier.fillMaxSize(),
        contentPadding = PaddingValues(16.dp),
        verticalArrangement = Arrangement.spacedBy(16.dp)
    ) {
        item {
            CalendarSection(
                selectedDate = selectedDate,
                onDateSelected = onDateSelected,
                currentMonth = currentMonth,
                onMonthChanged = onMonthChanged,
                usageData = usageData
            )
        }

        item {
            val dayData = usageData[selectedDate]
            if (dayData != null) {
                DayDetailsSection(dayData = dayData)
            } else {
                NoDataSection(selectedDate = selectedDate)
            }
        }
    }
}

@Composable
fun CalendarSection(
    selectedDate: LocalDate,
    onDateSelected: (LocalDate) -> Unit,
    currentMonth: YearMonth,
    onMonthChanged: (YearMonth) -> Unit,
    usageData: Map<LocalDate, DayUsageData>
) {
    Card(
        modifier = Modifier.fillMaxWidth(),
        colors = CardDefaults.cardColors(containerColor = Color(0xFFF9F9F9)),
        shape = RoundedCornerShape(12.dp)
    ) {
        Column(modifier = Modifier.padding(16.dp)) {
            // Ïõî ÎÑ§ÎπÑÍ≤åÏù¥ÏÖò
            Row(
                modifier = Modifier.fillMaxWidth(),
                horizontalArrangement = Arrangement.SpaceBetween,
                verticalAlignment = Alignment.CenterVertically
            ) {
                IconButton(
                    onClick = { onMonthChanged(currentMonth.minusMonths(1)) },
                    enabled = currentMonth > YearMonth.of(2020, 1)
                ) {
                    Icon(Icons.Rounded.ArrowBackIos, contentDescription = "Ïù¥Ï†Ñ Îã¨")
                }

                Text(
                    text = "${currentMonth.year}ÎÖÑ ${currentMonth.monthValue}Ïõî",
                    fontSize = 18.sp,
                    fontWeight = FontWeight.Bold
                )

                IconButton(
                    onClick = { onMonthChanged(currentMonth.plusMonths(1)) },
                    enabled = currentMonth < YearMonth.now()
                ) {
                    Icon(Icons.Rounded.ArrowForwardIos, contentDescription = "Îã§Ïùå Îã¨")
                }
            }

            Spacer(modifier = Modifier.height(16.dp))

            // ÏöîÏùº Ìó§Îçî
            Row(modifier = Modifier.fillMaxWidth()) {
                listOf("Ïùº", "Ïõî", "Ìôî", "Ïàò", "Î™©", "Í∏à", "ÌÜ†").forEach { day ->
                    Text(
                        text = day,
                        modifier = Modifier.weight(1f),
                        textAlign = TextAlign.Center,
                        fontSize = 14.sp,
                        fontWeight = FontWeight.Medium,
                        color = Color.DarkGray
                    )
                }
            }

            Spacer(modifier = Modifier.height(8.dp))

            // Îã¨Î†• Í∑∏Î¶¨Îìú
            val days = remember(currentMonth) {
                val first = currentMonth.atDay(1)
                val lastDay = currentMonth.lengthOfMonth()
                val offset = first.dayOfWeek.value % 7
                List(offset) { null } + (1..lastDay).map { currentMonth.atDay(it) }
            }

            LazyVerticalGrid(
                columns = GridCells.Fixed(7),
                modifier = Modifier.height(240.dp),
                verticalArrangement = Arrangement.spacedBy(4.dp),
                horizontalArrangement = Arrangement.spacedBy(4.dp),
                userScrollEnabled = false
            ) {
                items(days) { date ->
                    val isSelected = date == selectedDate
                    val isToday = date == LocalDate.now()
                    val isFuture = date != null && date.isAfter(LocalDate.now())
                    val hasData = date != null && usageData.containsKey(date)

                    Box(
                        modifier = Modifier
                            .size(32.dp)
                            .clip(CircleShape)
                            .background(
                                when {
                                    isSelected -> Color(0xFF4285F4)  // ÌååÎûÄÏÉâ
                                    isToday -> Color(0xFFE8F5E8)     // Ïó∞Ìïú Ï¥àÎ°ùÏÉâ
                                    hasData -> Color(0xFFE3F2FD)     // Ïó∞Ìïú ÌååÎûÄÏÉâ
                                    else -> Color.Transparent
                                }
                            )
                            .clickable(enabled = date != null && !isFuture) {
                                date?.let { onDateSelected(it) }
                            },
                        contentAlignment = Alignment.Center
                    ) {
                        Text(
                            text = date?.dayOfMonth?.toString() ?: "",
                            color = when {
                                isSelected -> Color.White
                                isFuture -> Color.Gray
                                isToday -> Color(0xFF4285F4)
                                else -> Color.Black
                            },
                            fontSize = 14.sp,
                            fontWeight = if (isSelected || isToday) FontWeight.Bold else FontWeight.Normal
                        )
                    }
                }
            }
        }
    }
}

