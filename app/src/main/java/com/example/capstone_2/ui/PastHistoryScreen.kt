private fun formatMinutesToTime(minutes: Int): String {
    val hours = minutes / 60
    val mins = minutes % 60
    return if (hours > 0) {
        String.format("%02d:%02d", hours, mins)
    } else {
        String.format("00:%02d", mins)
    }
}

